let currentLang = localStorage.getItem("portfolio-lang") || "en";

function applyLanguage(lang) {
    const elementsToTranslate = document.querySelectorAll("[data-key]");
    
    elementsToTranslate.forEach(element => {
        const key = element.getAttribute("data-key");
        if (translations[lang] && translations[lang][key]) {
            element.textContent = translations[lang][key];
        }
    });

    // Handle direction and language properties
    if (lang === "fa") {
        document.documentElement.setAttribute("dir", "rtl");
        document.documentElement.setAttribute("lang", "fa");
        document.getElementById("lang-btn").textContent = "EN";
    } else {
        document.documentElement.setAttribute("dir", "ltr");
        document.documentElement.setAttribute("lang", "en");
        document.getElementById("lang-btn").textContent = "FA";
    }
}

// Event Listener for Language Button Toggle
document.getElementById("lang-btn").addEventListener("click", () => {
    currentLang = currentLang === "en" ? "fa" : "en";
    localStorage.setItem("portfolio-lang", currentLang);
    applyLanguage(currentLang);
});

// Initial Load Configuration
document.addEventListener("DOMContentLoaded", () => {
    applyLanguage(currentLang);
    
    // Intersection Observer Configuration for Scroll Animations
    const faders = document.querySelectorAll(".fade-in-section");
    const appearOptions = {
        threshold: 0.1,
        rootMargin: "0px 0px -50px 0px"
    };

    const appearanceObserver = new IntersectionObserver(function(entries, observer) {
        entries.forEach(entry => {
            if (entry.isIntersecting) {
                entry.target.classList.add("is-visible");
                observer.unobserve(entry.target);
            }
        });
    }, appearOptions);

    faders.forEach(fader => {
        appearanceObserver.observe(fader);
    });
});
